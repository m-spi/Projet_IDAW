    <h1 class="enSavoirPlusTitle1">Protéines</h1>
    <p class="enSavoirPlusText">Les protéines sont des nutriments essentiels pour la croissance, la réparation et le bon fonctionnement de l'organisme. Cependant, une consommation excessive de protéines peut avoir des conséquences sur la santé. Il est crucial de respecter les recommandations de consommation de protéines pour les raisons suivantes :</p>
    <div class="reason">
        <h2 class="enSavoirPlusTitle2">Surcharges rénales</h2>
        <p class="enSavoirPlusText">Une consommation excessive de protéines peut exercer une charge supplémentaire sur les reins, ce qui peut entraîner des problèmes rénaux.</p>
    </div>
    <div class="reason">
        <h2 class="enSavoirPlusTitle2">Perte de calcium</h2>
        <p class="enSavoirPlusText">Un excès de protéines peut augmenter la perte de calcium dans les os, contribuant ainsi à l'ostéoporose.</p>
    </div>
    <div class="reason">
        <h2 class="enSavoirPlusTitle2">Problèmes digestifs</h2>
        <p class="enSavoirPlusText">Certaines personnes peuvent éprouver des problèmes digestifs, tels que la constipation, en raison d'une consommation excessive de protéines.</p>
    </div>
