            <h1 class="enSavoirPlusTitle1">Sel (Sodium)</h1>
            <p class="enSavoirPlusText">Le sel, également connu sous le nom de sodium, est un minéral essentiel que le corps a besoin pour fonctionner correctement. Cependant, une consommation excessive de sel peut avoir des effets néfastes sur la santé. Il est important de respecter le seuil recommandé de sel pour les raisons suivantes :</p>
            <div class="reason">
                <h2 class="enSavoirPlusTitle2">Pression artérielle élevée</h2>
                <p class="enSavoirPlusText">Une consommation excessive de sel peut entraîner une augmentation de la pression artérielle, ce qui augmente le risque de maladies cardiovasculaires, notamment les crises cardiaques et les accidents vasculaires cérébraux.</p>
            </div>
            <div class="reason">
                <h2 class="enSavoirPlusTitle2">Rétention d'eau</h2>
                <p class="enSavoirPlusText">Le sel retient l'eau dans le corps, ce qui peut entraîner un gonflement et un stress supplémentaire sur les reins.</p>
            </div>
            <div class="reason">
                <h2 enSavoirPlusTitle2>Maladies rénales</h2>
                <p class="enSavoirPlusText">Une forte consommation de sel peut augmenter le risque de maladies rénales, telles que les calculs rénaux.</p>
            </div>
            <div class="reason">
                <h2 class="enSavoirPlusTitle2">Ostéoporose</h2>
                <p class="enSavoirPlusText">Une alimentation riche en sel peut contribuer à la perte de calcium dans les os, augmentant ainsi le risque d'ostéoporose.</p>
            </div>
        </div>
