    <h1 class="enSavoirPlusTitle1">Sucre</h1>
    <p class="enSavoirPlusText">Le sucre est un glucide couramment utilisé comme édulcorant dans de nombreux aliments et boissons. Cependant, une consommation excessive de sucre peut avoir des conséquences néfastes sur la santé. Il est essentiel de respecter les recommandations de consommation de sucre pour les raisons suivantes :</p>
    <div class="reason">
        <h2 class="enSavoirPlusTitle2">Prise de poids</h2>
        <p class="enSavoirPlusText">La consommation excessive de sucre est souvent associée à une prise de poids, ce qui peut augmenter le risque d'obésité et de problèmes de santé connexes.</p>
    </div>
    <div class="reason">
        <h2 class="enSavoirPlusTitle2">Diabète de type 2</h2>
        <p class="enSavoirPlusText">Une alimentation riche en sucre peut augmenter le risque de développer un diabète de type 2, une maladie qui affecte la régulation de la glycémie.</p>
    </div>
    <div class="reason">
        <h2 class="enSavoirPlusTitle2">Problèmes dentaires</h2>
        <p class="enSavoirPlusText">Le sucre est un facteur de risque majeur de caries dentaires, car il favorise la croissance de bactéries dans la bouche.</p>
    </div>
    <div class="reason">
        <h2 class="enSavoirPlusTitle2">Dépendance au sucre</h2>
        <p class="enSavoirPlusText">Certaines personnes peuvent développer une dépendance au sucre, ce qui peut entraîner une alimentation déséquilibrée et des problèmes de santé.</p>
    </div>
